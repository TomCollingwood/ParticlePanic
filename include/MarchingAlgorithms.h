  /// \brief draw3DRealtime draws the m_realtime3DTriangle Vec3s using OpenGL glBegin(GL_TRIANGLES) (calculates normals as well)
  //----------------------------------------------------------------------------------------------------------------------
  void draw3DRealtime() ;

