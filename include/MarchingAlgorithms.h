  //----------------------------------------------------------------------------------------------------------------------
  void setSquareSize(const float ss);

  //----------------------------------------------------------------------------------------------------------------------
  /// \brief toggle3DResolution either multiplies or divides the 3d render resolution by 6 - used for high poly snapshot
  //----------------------------------------------------------------------------------------------------------------------
  void toggle3DResolution();

